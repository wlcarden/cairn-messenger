/// Maximum attestation-chain length (leaf + intermediates + root).
/// A real chain is 5 certs; this bounds the walk against a malformed or
/// adversarial over-long chain.
const MAX_CHAIN_LEN: usize = 10;

/// Minimum chain length: a leaf and a (self-signed) root.
const MIN_CHAIN_LEN: usize = 2;

/// The number of top-level fields in a `KeyDescription` SEQUENCE
/// (attestation v3+ layout, stable for `KeyMint`): version, attestation
/// security level, keymaster version, keymaster security level,
/// attestation challenge, unique id, software-enforced, hardware-enforced.
const KEY_DESCRIPTION_MIN_FIELDS: usize = 8;

/// `KeyMint` `purpose` value for signing (`KM_PURPOSE_SIGN`).
const KM_PURPOSE_SIGN: u64 = 2;

/// `KeyMint` `origin` value for an in-hardware-generated key
/// (`KM_ORIGIN_GENERATED`).
const KM_ORIGIN_GENERATED: u64 = 0;

/// `AuthorizationList` context tag for `purpose` (a SET OF INTEGER).
const AUTH_TAG_PURPOSE: u32 = 1;
/// `AuthorizationList` context tag for `origin` (an INTEGER).
const AUTH_TAG_ORIGIN: u32 = 702;
/// `AuthorizationList` context tag for `rootOfTrust` (a SEQUENCE).
const AUTH_TAG_ROOT_OF_TRUST: u32 = 704;

/// The attested security level of the key's secure element (the
/// `attestationSecurityLevel` field of `KeyDescription`).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum SecurityLevel {
    /// Software-only (no secure element). Rejected by
    /// [`verify_key_attestation`].
    Software,
    /// Trusted Execution Environment (TEE).
    TrustedEnvironment,
    /// Dedicated secure element (`StrongBox`).
    StrongBox,
}

impl SecurityLevel {
    /// Map the `KeyMint` `SecurityLevel` ENUMERATED value.
    const fn from_enum(value: u64) -> Option<Self> {
        match value {
            0 => Some(Self::Software),
            1 => Some(Self::TrustedEnvironment),
            2 => Some(Self::StrongBox),
            _ => None,
        }
    }

    /// Whether this level is a hardware-backed secure element (TEE or
    /// `StrongBox`) — the D0033 §2 acceptance set.
    #[must_use]
    pub const fn is_hardware(self) -> bool {
        matches!(self, Self::TrustedEnvironment | Self::StrongBox)
    }
}

/// The verified-boot state from the leaf's `RootOfTrust` (D0033 §3).
/// Parsed + surfaced advisorily in Stage 1; pinned in Stage 2.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum VerifiedBootState {
    /// Boot chain verified against the device's configured root of trust.
    Verified,
    /// Boot chain verified against a user-installed key (e.g. `GrapheneOS`
    /// with a locked bootloader + a self-installed AVB key).
    SelfSigned,
    /// Boot was not verified.
    Unverified,
    /// Boot verification failed.
    Failed,
}

impl VerifiedBootState {
    const fn from_enum(value: u64) -> Option<Self> {
        match value {
            0 => Some(Self::Verified),
            1 => Some(Self::SelfSigned),
            2 => Some(Self::Unverified),
            3 => Some(Self::Failed),
            _ => None,
        }
    }
}

/// Advisory verified-boot facts from the leaf `RootOfTrust` (D0033 §3).
///
/// NOT asserted in Stage 1; the `GrapheneOS` `verifiedBootKey` pin +
/// `deviceLocked` / `verifiedBootState` assertion is Stage 2.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct VerifiedBootInfo {
    /// Whether the bootloader is locked (`deviceLocked`).
    pub device_locked: bool,
    /// The verified-boot state.
    pub verified_boot_state: VerifiedBootState,
    /// The `verifiedBootKey` digest — the AVB key fingerprint. Carried
    /// so Stage 2 can pin the `GrapheneOS` key without re-parsing.
    pub verified_boot_key: Vec<u8>,
}

/// The facts a verified attestation establishes (D0033 §2). Returned by
/// [`verify_key_attestation`] only when all four §2 assertions hold.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct AttestationFacts {
    /// The `KeyMint` `attestationVersion` (e.g. 400 for `KeyMint`).
    pub attestation_version: u32,
    /// The attested security level — guaranteed `TrustedEnvironment` or
    /// `StrongBox` (Software is rejected).
    pub security_level: SecurityLevel,
    /// Always `true` on the Ok path: hardware-enforced
    /// `origin == GENERATED` (key minted in-hardware, not imported).
    pub origin_generated: bool,
    /// Always `true` on the Ok path: hardware-enforced
    /// `purpose ⊇ {SIGN}`.
    pub purpose_sign: bool,
    /// The leaf cert's Ed25519 device public key (32 bytes), when the
    /// leaf SPKI is Ed25519 (the Cairn device key always is). The caller
    /// can cross-check this equals the device key it expects, binding
    /// the attestation to a specific key. `None` if the leaf SPKI is not
    /// Ed25519.
    pub device_public_key: Option<Vec<u8>>,
    /// Advisory verified-boot facts (D0033 §3). `None` if absent or
    /// unparseable — Stage 1 never fails the verification on these.
    pub verified_boot: Option<VerifiedBootInfo>,
}

/// Why an attestation chain failed to verify. Variants carry indices /
/// tags only — never certificate or key bytes — per D0018 §4.2.
///
/// `#[non_exhaustive]` so Stage 2 (verified-boot pinning, revocation)
/// can add variants without a major bump.
#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub enum AttestationError {
    /// The chain was empty.
    EmptyChain,
    /// The chain had fewer than `MIN_CHAIN_LEN` certs.
    TooFewCertificates {
        /// Number of certs supplied.
        got: usize,
    },
    /// The chain had more than `MAX_CHAIN_LEN` certs.
    ChainTooLong {
        /// Number of certs supplied.
        got: usize,
    },
    /// Certificate at `index` (leaf = 0) did not parse as DER X.509.
    MalformedCertificate {
        /// Position in the chain (leaf-first).
        index: usize,
    },
    /// Certificate at `index` was outside its validity window at
    /// `now_unix`.
    ValidityWindowFailed {
        /// Position in the chain (leaf-first).
        index: usize,
    },
    /// The cert at `index` claims an issuer DN that does not match the
    /// subject DN of the next cert in the chain.
    IssuerSubjectMismatch {
        /// Position of the child cert in the chain (leaf-first).
        index: usize,
    },
    /// The signature on the cert at `index` did not verify against the
    /// next cert's public key (or, for the root, its own).
    LinkSignatureInvalid {
        /// Position of the child cert in the chain (leaf-first).
        index: usize,
    },
    /// The terminal cert is not self-signed (subject ≠ issuer), so it
    /// cannot be a trust-anchor root.
    RootNotSelfSigned,
    /// The terminal cert's `SubjectPublicKeyInfo` does not equal the
    /// pinned Google Hardware Attestation Root.
    RootNotPinned,
    /// The leaf carried no `KeyMint` attestation extension (OID
    /// `1.3.6.1.4.1.11129.2.1.17`) — the key was generated without a
    /// challenge, so no real chain exists.
    MissingKeymintExtension,
    /// The `KeyMint` extension was present but its `KeyDescription` ASN.1
    /// did not decode to the expected structure.
    MalformedKeymintExtension,
    /// The attested `attestationChallenge` did not equal the expected
    /// challenge — the chain does not bind our freshness nonce.
    ChallengeMismatch,
    /// The attested `attestationSecurityLevel` was `Software` — not a
    /// hardware-backed secure element.
    SecurityLevelTooLow,
    /// The hardware-enforced `origin` was not `GENERATED` — the key was
    /// imported or derived, not minted in-hardware.
    OriginNotGenerated,
    /// The hardware-enforced `purpose` set did not include `SIGN`.
    PurposeMissingSign,
}

impl core::fmt::Display for AttestationError {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        match self {
            Self::EmptyChain => write!(f, "attestation chain is empty"),
            Self::TooFewCertificates { got } => {
                write!(f, "attestation chain too short: {got} cert(s)")
            }
            Self::ChainTooLong { got } => write!(f, "attestation chain too long: {got} certs"),
            Self::MalformedCertificate { index } => {
                write!(f, "attestation cert {index} did not parse")
            }
            Self::ValidityWindowFailed { index } => {
                write!(f, "attestation cert {index} outside its validity window")
            }
            Self::IssuerSubjectMismatch { index } => {
                write!(f, "attestation cert {index} issuer/subject mismatch")
            }
            Self::LinkSignatureInvalid { index } => {
                write!(f, "attestation cert {index} signature did not verify")
            }
            Self::RootNotSelfSigned => write!(f, "attestation root is not self-signed"),
            Self::RootNotPinned => {
                write!(f, "attestation root is not the pinned Google root")
            }
            Self::MissingKeymintExtension => {
                write!(f, "leaf has no `KeyMint` attestation extension")
            }
            Self::MalformedKeymintExtension => write!(f, "`KeyMint` KeyDescription malformed"),
            Self::ChallengeMismatch => write!(f, "attestation challenge mismatch"),
            Self::SecurityLevelTooLow => write!(f, "attestation security level is software-only"),
            Self::OriginNotGenerated => write!(f, "attested key origin is not GENERATED"),
            Self::PurposeMissingSign => write!(f, "attested key purpose does not include SIGN"),
        }
    }
}

impl core::error::Error for AttestationError {}

/// Verify an Android Key Attestation chain per D0033 §2 (Stage 1).
///
/// `chain` is the DER-encoded cert chain, **leaf-first**, exactly as
/// Android `KeyStore.getCertificateChain` returns it (leaf → … →
/// self-signed Google root). `expected_challenge` is the 32-byte
/// freshness nonce embedded at key-generation. `now_unix` is the current
/// time (seconds) for the validity-window checks.
///
/// # Errors
///
/// Returns an [`AttestationError`] if any §2 check fails: a malformed or
/// expired cert, a broken signature link, a chain not rooted in the
/// pinned Google root, a missing/malformed `KeyMint` extension, a
/// challenge mismatch, a software-only security level, a non-`GENERATED`
/// origin, or a `purpose` set lacking `SIGN`.
pub fn verify_key_attestation(
    chain: &[Vec<u8>],
    expected_challenge: &[u8],
    now_unix: u64,
) -> Result<AttestationFacts, AttestationError> {
    if chain.is_empty() {
        return Err(AttestationError::EmptyChain);
    }
    if chain.len() < MIN_CHAIN_LEN {
        return Err(AttestationError::TooFewCertificates { got: chain.len() });
    }
    if chain.len() > MAX_CHAIN_LEN {
        return Err(AttestationError::ChainTooLong { got: chain.len() });
    }

    // Parse every cert up front (leaf-first). Keep them in a Vec so the
    // borrow lasts for the whole chain walk.
    let certs: Vec<X509Certificate> = chain
        .iter()
        .enumerate()
        .map(|(index, der)| {
            X509Certificate::from_der(der)
                .map(|(_, cert)| cert)
                .map_err(|_| AttestationError::MalformedCertificate { index })
        })
        .collect::<Result<_, _>>()?;

    // (1) Validity windows: every cert must be valid at `now_unix`.
    let now =
        i64::try_from(now_unix).map_err(|_| AttestationError::ValidityWindowFailed { index: 0 })?;
    for (index, cert) in certs.iter().enumerate() {
        let not_before = cert.validity().not_before.timestamp();
        let not_after = cert.validity().not_after.timestamp();
        if now < not_before || now > not_after {
            return Err(AttestationError::ValidityWindowFailed { index });
        }
    }

    // (1) Signature links: each child must be signed by the next cert's
    // key, and the child's issuer DN must equal the parent's subject DN.
    for (index, pair) in certs.windows(2).enumerate() {
        let [child, parent] = pair else {
            // windows(2) always yields len-2 slices; defensive only.
            return Err(AttestationError::LinkSignatureInvalid { index });
        };
        if child.issuer() != parent.subject() {
            return Err(AttestationError::IssuerSubjectMismatch { index });
        }
        child
            .verify_signature(Some(parent.public_key()))
            .map_err(|_| AttestationError::LinkSignatureInvalid { index })?;
    }

    // (2) The terminal cert must be a self-signed root that equals the
    // pinned Google Hardware Attestation Root.
    let root_index = certs.len().saturating_sub(1);
    let root = certs
        .last()
        .ok_or(AttestationError::TooFewCertificates { got: certs.len() })?;
    if root.subject() != root.issuer() {
        return Err(AttestationError::RootNotSelfSigned);
    }
    root.verify_signature(Some(root.public_key()))
        .map_err(|_| AttestationError::LinkSignatureInvalid { index: root_index })?;
    if root.public_key().raw != PINNED_GOOGLE_ROOT_SPKI_DER {
        return Err(AttestationError::RootNotPinned);
    }

    // (3) + (4) Parse the leaf's `KeyMint` extension + assert the four
    // §2 properties.
    let leaf = certs.first().ok_or(AttestationError::EmptyChain)?;
    let key_desc = leaf_keymint_extension(leaf)?;
    let parsed = parse_key_description(key_desc)?;

    if parsed.challenge != expected_challenge {
        return Err(AttestationError::ChallengeMismatch);
    }
    if !parsed.security_level.is_hardware() {
        return Err(AttestationError::SecurityLevelTooLow);
    }
    if !parsed.origin_generated {
        return Err(AttestationError::OriginNotGenerated);
    }
    if !parsed.purpose_sign {
        return Err(AttestationError::PurposeMissingSign);
    }

    Ok(AttestationFacts {
        attestation_version: parsed.attestation_version,
        security_level: parsed.security_level,
        origin_generated: parsed.origin_generated,
        purpose_sign: parsed.purpose_sign,
        device_public_key: leaf_ed25519_key(leaf),
        verified_boot: parsed.verified_boot,
    })
}

/// Pull the `KeyMint` `KeyDescription` DER (the extnValue content) from the
/// leaf's `1.3.6.1.4.1.11129.2.1.17` extension.
fn leaf_keymint_extension<'a>(leaf: &'a X509Certificate<'a>) -> Result<&'a [u8], AttestationError> {
    let keymint_oid = oid!(1.3.6.1.4.1.11129.2.1.17);
    let ext = leaf
        .get_extension_unique(&keymint_oid)
        .map_err(|_| AttestationError::MalformedKeymintExtension)?
        .ok_or(AttestationError::MissingKeymintExtension)?;
    Ok(ext.value)
}

/// Extract the leaf's Ed25519 device public key (32 bytes), if the leaf
/// SPKI is Ed25519. Best-effort: returns `None` for any other key type.
fn leaf_ed25519_key(leaf: &X509Certificate) -> Option<Vec<u8>> {
    let spki = leaf.public_key();
    if spki.algorithm.algorithm != OID_SIG_ED25519 {
        return None;
    }
    let key = spki.subject_public_key.data.as_ref();
    if key.len() == 32 {
        Some(key.to_vec())
    } else {
        None
    }
}

/// The fields extracted from a `KeyDescription` before the §2 assertions.
struct ParsedKeyDescription<'a> {
    attestation_version: u32,
    security_level: SecurityLevel,
    challenge: &'a [u8],
    origin_generated: bool,
    purpose_sign: bool,
    verified_boot: Option<VerifiedBootInfo>,
}

/// Decode the `KeyMint` `KeyDescription` SEQUENCE (D0033 §2 step 3).
///
/// The top-level fields are a fixed, ordered SEQUENCE so they are read
/// positionally; the `AuthorizationList` entries are sparse EXPLICIT
/// context-tagged optionals so they are matched by tag number.
fn parse_key_description(der: &[u8]) -> Result<ParsedKeyDescription<'_>, AttestationError> {
    // `der` is the KeyDescription SEQUENCE TLV (the extnValue content);
    // unwrap it to its content before iterating the ordered fields.
    let outer = der_single_element(der)?;
    if outer.header.tag() != Tag::Sequence {
        return Err(AttestationError::MalformedKeymintExtension);
    }
    let fields = der_sequence_elements(outer.data)?;
    if fields.len() < KEY_DESCRIPTION_MIN_FIELDS {
        return Err(AttestationError::MalformedKeymintExtension);
    }

    // [0] attestationVersion (INTEGER)
    let attestation_version =
        u32::try_from(read_universal_uint(field_at(&fields, 0)?, Tag::Integer)?)
            .map_err(|_| AttestationError::MalformedKeymintExtension)?;

    // [1] attestationSecurityLevel (ENUMERATED)
    let sec_value = read_universal_uint(field_at(&fields, 1)?, Tag::Enumerated)?;
    let security_level =
        SecurityLevel::from_enum(sec_value).ok_or(AttestationError::MalformedKeymintExtension)?;

    // [4] attestationChallenge (OCTET STRING)
    let challenge_any = field_at(&fields, 4)?;
    if challenge_any.header.tag() != Tag::OctetString {
        return Err(AttestationError::MalformedKeymintExtension);
    }
    let challenge = challenge_any.data;

    // [7] hardwareEnforced (SEQUENCE of EXPLICIT context-tagged entries)
    let hw = field_at(&fields, 7)?;
    if hw.header.tag() != Tag::Sequence {
        return Err(AttestationError::MalformedKeymintExtension);
    }
    let (origin_generated, purpose_sign, verified_boot) = parse_authorization_list(hw.data)?;

    Ok(ParsedKeyDescription {
        attestation_version,
        security_level,
        challenge,
        origin_generated,
        purpose_sign,
        verified_boot,
    })
}

/// Walk a hardware-enforced `AuthorizationList`, extracting the
/// asserted-on tags ([`AUTH_TAG_ORIGIN`], [`AUTH_TAG_PURPOSE`]) and the
/// advisory `RootOfTrust` ([`AUTH_TAG_ROOT_OF_TRUST`]).
///
/// Returns `(origin_generated, purpose_sign, verified_boot)`. Absent tags
/// yield `false` / `None` — the caller turns a missing `origin`/`purpose`
/// into the appropriate §4 rejection.
fn parse_authorization_list(
    der: &[u8],
) -> Result<(bool, bool, Option<VerifiedBootInfo>), AttestationError> {
    let entries = der_sequence_elements(der)?;
    let mut origin_generated = false;
    let mut purpose_sign = false;
    let mut verified_boot = None;

    for entry in &entries {
        // AuthorizationList entries are EXPLICIT [tag] context-specific.
        if entry.header.class() != Class::ContextSpecific {
            continue;
        }
        match entry.header.tag().0 {
            AUTH_TAG_PURPOSE => {
                // EXPLICIT wrap → inner SET OF INTEGER.
                let inner = der_single_element(entry.data)?;
                if inner.header.tag() != Tag::Set {
                    return Err(AttestationError::MalformedKeymintExtension);
                }
                for int_any in der_sequence_elements(inner.data)? {
                    if int_any.header.tag() == Tag::Integer
                        && read_uint_bytes(int_any.data) == Some(KM_PURPOSE_SIGN)
                    {
                        purpose_sign = true;
                    }
                }
            }
            AUTH_TAG_ORIGIN => {
                // EXPLICIT wrap → inner INTEGER.
                let inner = der_single_element(entry.data)?;
                if inner.header.tag() == Tag::Integer
                    && read_uint_bytes(inner.data) == Some(KM_ORIGIN_GENERATED)
                {
                    origin_generated = true;
                }
            }
            AUTH_TAG_ROOT_OF_TRUST => {
                // Advisory (D0033 §3): best-effort, never fail on this.
                verified_boot = parse_root_of_trust(entry.data).ok();
            }
            _ => {}
        }
    }

    Ok((origin_generated, purpose_sign, verified_boot))
}

/// Parse the `RootOfTrust` SEQUENCE (D0033 §3, advisory). The EXPLICIT
/// [704] wrap's content is a SEQUENCE:
/// `{ verifiedBootKey OCTET STRING, deviceLocked BOOLEAN,
///    verifiedBootState ENUMERATED, verifiedBootHash OCTET STRING }`.
fn parse_root_of_trust(explicit_inner: &[u8]) -> Result<VerifiedBootInfo, AttestationError> {
    let seq = der_single_element(explicit_inner)?;
    if seq.header.tag() != Tag::Sequence {
        return Err(AttestationError::MalformedKeymintExtension);
    }
    let parts = der_sequence_elements(seq.data)?;
    let key_any = field_at(&parts, 0)?;
    if key_any.header.tag() != Tag::OctetString {
        return Err(AttestationError::MalformedKeymintExtension);
    }
    let locked_any = field_at(&parts, 1)?;
    // BOOLEAN content: 0x00 = false, anything else = true.
    let device_locked = locked_any.data.first().is_some_and(|&b| b != 0x00);
    let state_value = read_universal_uint(field_at(&parts, 2)?, Tag::Enumerated)?;
    let verified_boot_state = VerifiedBootState::from_enum(state_value)
        .ok_or(AttestationError::MalformedKeymintExtension)?;

    Ok(VerifiedBootInfo {
        device_locked,
        verified_boot_state,
        verified_boot_key: key_any.data.to_vec(),
    })
}

/// Parse a DER buffer as a flat sequence of TLV elements (the content of
/// a SEQUENCE/SET). Errors if any element is malformed or trailing bytes
/// remain that do not form a complete element.
fn der_sequence_elements(mut input: &[u8]) -> Result<Vec<Any<'_>>, AttestationError> {
    let mut out = Vec::new();
    while !input.is_empty() {
        let (rem, any) =
            Any::from_der(input).map_err(|_| AttestationError::MalformedKeymintExtension)?;
        out.push(any);
        input = rem;
    }
    Ok(out)
}

/// Parse exactly one DER TLV element from the front of `input` (used to
/// unwrap an EXPLICIT context tag's inner element).
fn der_single_element(input: &[u8]) -> Result<Any<'_>, AttestationError> {
    let (_, any) = Any::from_der(input).map_err(|_| AttestationError::MalformedKeymintExtension)?;
    Ok(any)
}

/// Borrow the `idx`-th element of a parsed field list, or error.
fn field_at<'b, 'a>(fields: &'b [Any<'a>], idx: usize) -> Result<&'b Any<'a>, AttestationError> {
    fields
        .get(idx)
        .ok_or(AttestationError::MalformedKeymintExtension)
}

/// Read a small unsigned integer from a universal-tagged element,
/// asserting its tag.
fn read_universal_uint(any: &Any, expected: Tag) -> Result<u64, AttestationError> {
    if any.header.tag() != expected {
        return Err(AttestationError::MalformedKeymintExtension);
    }
    read_uint_bytes(any.data).ok_or(AttestationError::MalformedKeymintExtension)
}

/// Interpret up to 8 big-endian content bytes as an unsigned integer.
/// DER INTEGER content may carry a leading `0x00` sign byte (e.g. keySize
/// `0x0100` = 256); this folds it in. Returns `None` for empty or
/// over-wide (> 8 byte) content.
fn read_uint_bytes(data: &[u8]) -> Option<u64> {
    if data.is_empty() || data.len() > 8 {
        return None;
    }
    let mut value: u64 = 0;
    for &b in data {
        value = (value << 8) | u64::from(b);
    }
    Some(value)
}

