//! Android Key Attestation verification per D0033 (Stage 1).
//!
//! [`verify_key_attestation`] cryptographically PROVES that the device
//! signing key lives in secure hardware (TEE / `StrongBox`), is
//! non-exportable, and was generated in-hardware — by verifying the
//! X.509 attestation certificate chain that `KeyMint` emits at
//! key-generation time, rather than trusting the Kotlin
//! `KeyInfo.securityLevel` self-report (D0020 §3.8, realizing the
//! `KeystoreEd25519Signer.securityLevelOf` follow-up).
//!
//! ## What a verified chain proves (D0033 "what attestation proves")
//!
//! Android Key Attestation issues, at key-generation time, a cert chain
//! `leaf (device-key cert, carrying the KeyMint extension)` →
//! `intermediate(s)` → `Google Hardware Attestation Root`. The leaf's
//! extension (OID `1.3.6.1.4.1.11129.2.1.17`, a `KeyDescription`
//! SEQUENCE) is signed by the secure element's attestation key, which
//! chains to Google's root. So a verified chain proves the key was
//! generated by genuine Google-attested `KeyMint` hardware, with the
//! attested properties (security level, origin, purpose, the challenge).
//!
//! It does NOT prove the key is unseizable or the OS is uncompromised —
//! only that the _key material_ is hardware-bound + non-exportable as
//! attested. The `GrapheneOS` verified-boot binding (D0033 §3, Stage 2)
//! extends it to "this is `GrapheneOS` with a locked bootloader"; Stage 1
//! parses + surfaces those `RootOfTrust` fields advisorily (see
//! [`VerifiedBootInfo`]) but does not yet pin them.
//!
//! ## Stage 1 checks (D0033 §2)
//!
//! 1. Parse each DER cert; verify each leaf→…→root link signature with
//!    `x509-parser`'s `ring`-backed `verify_signature`, and each cert's
//!    validity window against `now_unix`.
//! 2. Check the chain terminates at a self-signed cert whose
//!    `SubjectPublicKeyInfo` equals the **pinned Google Hardware
//!    Attestation Root** (`PINNED_GOOGLE_ROOT_SPKI_DER`).
//! 3. Extract + ASN.1-decode the leaf's `KeyMint` `KeyDescription`.
//! 4. Assert: `attestationChallenge == expected_challenge`;
//!    `attestationSecurityLevel ∈ {TrustedEnvironment, StrongBox}`;
//!    hardware-enforced `origin == GENERATED`; hardware-enforced
//!    `purpose ⊇ {SIGN}`.
//!
//! On success returns [`AttestationFacts`]; any failure is a uniform
//! [`AttestationError`] variant carrying indices/tags only (no payload
//! bytes, per D0018 §4.2). The **caller** decides posture: per D0033 §4
//! a failed/absent attestation downgrades the device key to "unattested"
//! and the app keeps running (it falls back to a software key) — the
//! verifier itself is strict (Ok ⟺ all four assertions hold).

// `FromDer` is the single asn1-rs trait both `Any::from_der` and
// `X509Certificate::from_der` resolve through (x509-parser's prelude
// re-exports `asn1_rs::FromDer`); import it once.
use x509_parser::der_parser::asn1_rs::{Any, Class, FromDer, Tag};
use x509_parser::der_parser::oid;
use x509_parser::oid_registry::OID_SIG_ED25519;
use x509_parser::prelude::X509Certificate;

/// The pinned **Google Hardware Attestation Root** `SubjectPublicKeyInfo`
/// (DER), the offline trust anchor for the attestation chain per D0033
