    /// Synthetic DER builders for the decode tests + the pin test. These
    /// produce *only* test inputs — never anything the verifier trusts.
    ///
    /// The `len as u8` casts are guarded by the surrounding length-form
    /// branches (a DER short/0x81 length byte is < 256 by construction),
    /// so the truncation is intentional and test-local.
    #[allow(clippy::cast_possible_truncation)]
    mod synthetic {
        use rcgen::{
            BasicConstraints, CertificateParams, DnType, IsCa, KeyPair, PKCS_ECDSA_P384_SHA384,
            date_time_ymd,
        };

        /// Encode a single DER TLV with a one-byte tag, handling the
        /// short (< 128), one-byte-long (< 256), and two-byte-long
        /// length forms.
        pub(super) fn tlv(tag: u8, body: &[u8]) -> Vec<u8> {
            let mut v = vec![tag];
            let len = body.len();
            if len < 0x80 {
                v.push(len as u8);
            } else if len < 0x100 {
                v.push(0x81);
                v.push(len as u8);
            } else {
                v.push(0x82);
                v.push((len >> 8) as u8);
                v.push((len & 0xFF) as u8);
            }
            v.extend_from_slice(body);
            v
        }

        /// Encode a context-specific constructed (EXPLICIT) tag header,
        /// using the high-tag-number form for tags ≥ 31 (e.g. [702]).
        fn ctx_tag_bytes(num: u32) -> Vec<u8> {
            if num < 31 {
                return vec![0xA0 | (num as u8)];
            }
            let mut stack = vec![(num & 0x7F) as u8];
            let mut n = num >> 7;
            while n > 0 {
                stack.push(((n & 0x7F) as u8) | 0x80);
                n >>= 7;
            }
            stack.reverse();
            let mut out = vec![0xBF];
            out.extend_from_slice(&stack);
            out
        }

        /// Wrap `inner` in an EXPLICIT context tag `num`. Inner bodies in
        /// these tests are always < 256 bytes (short or 0x81 length form).
        fn ctx_explicit(num: u32, inner: &[u8]) -> Vec<u8> {
            let mut v = ctx_tag_bytes(num);
            let len = inner.len();
            if len >= 0x80 {
                v.push(0x81);
            }
            v.push(len as u8);
            v.extend_from_slice(inner);
            v
        }

        fn integer(value: u64) -> Vec<u8> {
            // Minimal big-endian; 0 encodes as a single 0x00 byte.
            if value == 0 {
                return tlv(0x02, &[0x00]);
            }
            let mut bytes = value.to_be_bytes().to_vec();
            while bytes.first() == Some(&0x00) {
                bytes.remove(0);
            }
            tlv(0x02, &bytes)
        }

        /// Build a `KeyMint` `KeyDescription` SEQUENCE with the given
        /// attestation security level (ENUMERATED), hardware-enforced
        /// origin, hardware-enforced purpose set, and challenge.
        pub(super) fn key_description(
            sec_level: u8,
            origin: u64,
            purposes: &[u64],
            challenge: &[u8],
        ) -> Vec<u8> {
            // hardwareEnforced AuthorizationList: [1] purpose (SET OF
            // INTEGER) then [702] origin (INTEGER).
            let purpose_set: Vec<u8> = {
                let mut ints = Vec::new();
                for &p in purposes {
                    ints.extend_from_slice(&integer(p));
                }
                tlv(0x31, &ints) // SET
            };
            let mut hw = Vec::new();
            hw.extend_from_slice(&ctx_explicit(1, &purpose_set));
            hw.extend_from_slice(&ctx_explicit(702, &integer(origin)));
            let hw_seq = tlv(0x30, &hw);

            let mut kd = Vec::new();
            kd.extend_from_slice(&integer(400)); // [0] attestationVersion
            kd.extend_from_slice(&tlv(0x0A, &[sec_level])); // [1] secLevel ENUM
            kd.extend_from_slice(&integer(400)); // [2] keymasterVersion
            kd.extend_from_slice(&tlv(0x0A, &[sec_level])); // [3] kmSecLevel
            kd.extend_from_slice(&tlv(0x04, challenge)); // [4] challenge OCTET
            kd.extend_from_slice(&tlv(0x04, &[])); // [5] uniqueId OCTET (empty)
            kd.extend_from_slice(&tlv(0x30, &[])); // [6] softwareEnforced (empty)
            kd.extend_from_slice(&hw_seq); // [7] hardwareEnforced
            tlv(0x30, &kd)
        }

        /// A self-signed P-384 root + a leaf signed by it, both valid in
        /// 2020–2030. The links verify and the root is self-signed, but
        /// the root SPKI is NOT the pinned Google root — the input for
        /// the `RootNotPinned` pin test. Returns `(leaf_der, root_der)`.
        pub(super) fn unpinned_chain() -> (Vec<u8>, Vec<u8>) {
            let root_key = KeyPair::generate_for(&PKCS_ECDSA_P384_SHA384).unwrap();
            let mut root_params = CertificateParams::default();
            root_params.is_ca = IsCa::Ca(BasicConstraints::Unconstrained);
            root_params.not_before = date_time_ymd(2020, 1, 1);
            root_params.not_after = date_time_ymd(2030, 1, 1);
            root_params
                .distinguished_name
                .push(DnType::CommonName, "Not The Google Root");
            let root = root_params.self_signed(&root_key).unwrap();

            let leaf_key = KeyPair::generate_for(&PKCS_ECDSA_P384_SHA384).unwrap();
            let mut leaf_params = CertificateParams::default();
            leaf_params.is_ca = IsCa::NoCa;
            leaf_params.not_before = date_time_ymd(2020, 1, 1);
            leaf_params.not_after = date_time_ymd(2030, 1, 1);
            leaf_params
                .distinguished_name
                .push(DnType::CommonName, "Synthetic Leaf");
            let leaf = leaf_params.signed_by(&leaf_key, &root, &root_key).unwrap();

            (leaf.der().as_ref().to_vec(), root.der().as_ref().to_vec())
        }
    }
